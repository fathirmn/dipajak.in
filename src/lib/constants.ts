export function getUnitCode(unit: string): string {
  const normalized = unit.toLowerCase().trim();
  return UNIT_CODES[normalized] || "UM.0033";
}

export function calculateDppNilaiLain(dpp: number, originalVatRate: number): number {
  if (originalVatRate === 12) return dpp;
  return Math.round(dpp * originalVatRate / 12);
}
